    private Bitmap GetBitmap(char c, params string[] data)
    {
        var bitmap = new Bitmap(8, 8);

        for (var y = 0; y < 8; y++)
        {
            for (var x = 0; x < 8; x++)
            {
                var pixel = data[y][x];
                bitmap.SetPixel(x, y, pixel == '*' ? Color.White : Color.Transparent);
            }
        }

        _characters[c] = bitmap;
        return bitmap;
    }
